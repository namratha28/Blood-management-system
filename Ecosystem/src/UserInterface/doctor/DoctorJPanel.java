    private void populateAvailTable() {
        DefaultTableModel model =(DefaultTableModel)availTable.getModel();
        
         model.setRowCount(0);
         for(TimeSlot ts:userAccount.getEmployee().getAvailability()){
            Object[] row = new Object[2];
            row[0] = ts.getFrom();
            row[1] = ts.getTo();
            model.addRow(row);
         }
        
    }
