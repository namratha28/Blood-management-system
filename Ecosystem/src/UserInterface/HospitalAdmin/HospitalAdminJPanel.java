    private void populateCombo() {
        OrgTypejComboBox.removeAllItems();

        for (Organization.Type type : Organization.Type.values()) {
            if (type == Organization.Type.Common || type == Organization.Type.Doctor || type == Organization.Type.FrontDesk || type == Organization.Type.Nurse || type == Organization.Type.Admin || type == Organization.Type.Lab) {
                OrgTypejComboBox.addItem(type);
            }
        }
    }

    private void populateOrgTable() {
        DefaultTableModel model = (DefaultTableModel) orgTable.getModel();

        model.setRowCount(0);

        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            Object[] row = new Object[2];
            row[0] = org;
            row[1] = org.getOrganizationID();
            model.addRow(row);
        }

    }

    public void populateEmployeejTable() {
        DefaultTableModel model = (DefaultTableModel) employeejTable.getModel();
        model.setRowCount(0);

        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (Employee e : org.getEmployeeDirectory().getEmployeeList()) {
                Object[] row = new Object[4];
                row[0] = e;
                row[1] = e.getSpecialities();
                row[2] = e.getRanking();
                model.addRow(row);
            }

        }
    }

    private void populateWrTable() {

        DefaultTableModel model = (DefaultTableModel) wrTable.getModel();
        model.setRowCount(0);

        for (WorkRequest r : enterprise.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[4];
            row[0] = r.getSender();
            row[1] = r.getReceiver();
            row[2] = r.getMessage();
            row[3] = r.getStatus();
            model.addRow(row);

        }
    }
